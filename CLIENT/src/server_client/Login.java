    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
           
            Socket socket =new Socket( "192.168.1.104",7800);      
       
      
           ObjectOutputStream dout  =new ObjectOutputStream(socket.getOutputStream());
           ObjectInputStream din = new ObjectInputStream(socket.getInputStream());
         
              String msgout  ="" ;
              String use = user.getText().trim() ;
              String pass = passe.getText().trim() ;
              msgout = "login " + use + " "+pass +"\n";
              dout.writeObject(msgout);                       
              String msgin  = "" ;
                 
              while(!"error login\n".equalsIgnoreCase(msgin) && !"ok login\n".equalsIgnoreCase(msgin))
              {
                  try { 
                      msgin = (String) din.readObject();
                  } catch (ClassNotFoundException ex) {
                      Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                  }
             
              }                      
              if ("ok login\n".equalsIgnoreCase(msgin)) {  
                 
                   
                    new chat_client(socket,din ,dout,use , pass).setVisible(true);                    
                    this.setVisible(false);
                        
             }else{
             
              JOptionPane.showMessageDialog(null, "Login Error ", "Error: ", JOptionPane.ERROR_MESSAGE);
              user.setText("");
              passe.setText("");
              

              
              socket.close();
              
             
             }
         } catch (IOException ex) {
           JOptionPane.showMessageDialog(null, "Un probleme de connection avec le serveur","Worning: ", JOptionPane.WARNING_MESSAGE);
         }
    }//GEN-LAST:event_jButton1ActionPerformed

